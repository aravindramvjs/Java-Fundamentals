package day08.practice;

//Write a Java program that prints the first 10 natural numbers in reverse order using a while loop.

public class ReverseNaturalNumbers {

	public static void main(String[] args) {

		int i = 10;					//initialing i 
		
		while(i >= 1) {				//condition
			
			System.out.println(i);	//print i
			
			i--;					//decrement
		}

	}

}
